
public class Bars {
	
	private int x, y , width, height;
	
	public Bars(int x, int y, int height, int width) {
		this.x=x;
		this.y=y;
		this.height =height;
		this.width=width;
	}
	
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}

}
